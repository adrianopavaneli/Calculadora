    public void operacaoAritimetica(){
        switch (calculation) {
            
            case 1: //Soma
                ans = num + Double.parseDouble(TxtVisor.getText());
                TxtVisor.setText(Double.toString(ans));
                break;
                case 2: //Multiplicação
                ans = num * Double.parseDouble(TxtVisor.getText());
                TxtVisor.setText(Double.toString(ans));
                break;
                case 3: //Divisao
                ans = num / Double.parseDouble(TxtVisor.getText());
                TxtVisor.setText(Double.toString(ans));
                break;
                case 4: //Subtracao
                ans = num - Double.parseDouble(TxtVisor.getText());
                TxtVisor.setText(Double.toString(ans));
                break;
            
        }
    }
    
    
    
